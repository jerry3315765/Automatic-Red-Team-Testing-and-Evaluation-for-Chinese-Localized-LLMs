def load_system_prompts():
    """Load system prompts (Jailbreak Templates) from data/raw folder"""
    system_prompts_path = os.path.join(BASE_DIR, '..', 'data', 'raw', 'attackSystemPromptChinese')
    if os.path.exists(system_prompts_path):
        prompts = {}
        for file in os.listdir(system_prompts_path):
            if file.endswith('.txt'):
                with open(os.path.join(system_prompts_path, file), 'r', encoding='utf-8') as f:
                    prompt_name = file.replace('.txt', '').replace('translated_', '')
                    prompts[prompt_name] = f.read().strip()
        return prompts
    return {}

def run_multi_turn_redteam_tests(scenarios, models_config, system_prompts, output_dir, max_turns=4):
    # If no system prompts, run with empty prompt
    if not system_prompts:
        system_prompts = {'no_attack': ''}

    # Iterate models first to minimize switching
    for model_cfg in models_config['models']:
        print(f"\n==================================================")
        print(f"Testing Model: {model_cfg['name']}")
        print(f"==================================================")

        if model_cfg['type'] == 'local':
            try:
                print(f"Loading model {model_cfg['name']}...")
                llm = LocalLLM(model_cfg)
                print(f"Model {model_cfg['name']} loaded. Waiting 15 seconds before starting tests...")
                time.sleep(15)  # Wait 15 seconds after model loading

                # Then iterate system prompts
                for prompt_name, system_prompt in system_prompts.items():
                    print(f"\n=== System Prompt: {prompt_name} | Model: {model_cfg['name']} ===")

                    # 1. Resume Capability: Check for existing results to skip
                    existing_scenarios = set()
                    output_file = os.path.join(output_dir, f"redteam_divi_results_{prompt_name}.json")
                    if os.path.exists(output_file):
                        try:
                            with open(output_file, 'r', encoding='utf-8') as f:
                                existing_data = json.load(f)
                                for entry in existing_data:
                                    # If this model has already recorded a result for this scenario, mark it as done
                                    if entry.get('model') == model_cfg['name']:
                                        existing_scenarios.add(entry.get('scenario_id'))
                            print(f"Found {len(existing_scenarios)} existing completed scenarios for this model (will be skipped).")
                        except Exception as e:
                            print(f"Error reading existing file for resume check: {e}")

                    # Then iterate scenarios
                    for scenario in scenarios:
                        # Skip if already done
                        if scenario['id'] in existing_scenarios:
                            print(f"Skipping scenario {scenario['id']} (already exists)")
                            continue

                        print(f"Running scenario {scenario['id']}: {scenario['description']}")
                        
                        # Initialize conversation history for the model (messages list)
                        model_messages = []
                        if system_prompt:
                            model_messages.append({"role": "system", "content": system_prompt})
                        
                        # Initialize generic history for logging
                        conversation_history = []
                        
                        scenario_results = [] # Store results just for this scenario

                        for turn in range(max_turns):
                            # Get the specific step/question for this turn
                            step_index = min(turn, len(scenario['steps'])-1)
                            step_content = scenario['steps'][step_index]

                            if turn == 0:
                                # First turn: use scenario context + first step
                                current_user_content = f"{scenario['context']}\n{step_content}"
                            else:
                                # Subsequent turns: just the next step/question
                                current_user_content = step_content

                            # Append user message to model history
                            model_messages.append({"role": "user", "content": current_user_content})

                            # Generate response
                            response_result = llm.generate([{
                                'prompt': model_messages, # Pass the full list of messages
                                'template_id': f"scenario_{scenario['id']}_turn_{turn+1}",
                                'intent': 'red_team_attack'
                            }])[0]

                            response = response_result['response']
                            
                            # Append assistant response to model history for next turn context
                            model_messages.append({"role": "assistant", "content": response})
                            
                            conversation_history.append({
                                'turn': turn + 1,
                                'prompt': current_user_content,
                                'response': response
                            })

                            # Evaluate using NEW GPT-4o-mini logic
                            eval_result = evaluate_turn_gpt4(scenario['description'], current_user_content, response)

                            result = {
                                'scenario_id': scenario['id'],
                                'scenario_desc': scenario['description'],
                                'attack_type': 'multi_turn_jailbreak',
                                'system_prompt_name': prompt_name,
                                'system_prompt': system_prompt,
                                'turn': turn + 1,
                                'prompt': current_user_content,
                                'response': response,
                                'evaluation': eval_result,
                                'model': model_cfg['name'],
                                'success': eval_result.get('success', False),
                                'conversation_history': conversation_history.copy()
                            }
                            scenario_results.append(result)

                            # Stop if we achieved the target harmful output (success=True)
                            if eval_result.get('success', False):
                                print(f"  -> Jailbreak Successful! ({eval_result.get('score')}/5)")
                                
                        # Save result IMMEDIATELY after each scenario finishes
                        # This ensures that even if it crashes in the next scenario, this one is saved.
                        if scenario_results:
                            print(f"Saving results for Scenario {scenario['id']}...")
                            save_results_to_file(scenario_results, output_dir)
                
                # Cleanup model after all prompts and scenarios are done for this model
                del llm
                if torch.cuda.is_available():
                    torch.cuda.empty_cache()

            except Exception as e:
                print(f"Error with model {model_cfg['name']}: {e}")
                import traceback
                traceback.print_exc()

    return []


def save_results_to_file(results, output_dir):
    """
    Appends new results to the appropriate JSON file based on system prompt.
    Does NOT perform clustering to save time.
    """
    # Group results by system prompt
    prompt_groups = {}
    for result in results:
        prompt_name = result.get('system_prompt_name', 'unknown')
        if prompt_name not in prompt_groups:
            prompt_groups[prompt_name] = []
        prompt_groups[prompt_name].append(result)

    for prompt_name, group_results in prompt_groups.items():
        if not group_results:
            continue

        filename = f"redteam_divi_results_{prompt_name}.json"
        file_path = os.path.join(output_dir, filename)
        
        existing_data = []
        if os.path.exists(file_path):
            try:
                with open(file_path, 'r', encoding='utf-8') as f:
                    existing_data = json.load(f)
            except Exception as e:
                print(f"Error reading existing file {file_path}: {e}")
        
        existing_data.extend(group_results)
        
        try:
            with open(file_path, 'w', encoding='utf-8') as f:
                json.dump(existing_data, f, ensure_ascii=False, indent=2)
            print(f"Saved {len(group_results)} new results to {filename}")
        except Exception as e:
            print(f"Error saving to {file_path}: {e}")


def run_clustering_on_all_files(output_dir):
    """
    Iterates through all result JSON files in output_dir and results applies DIVI clustering.
    This is run once at the end of the experiment.
    """
    print("\nStarting final clustering for all collected results...")
    
    # Find all result files
    files = [f for f in os.listdir(output_dir) if f.startswith("redteam_divi_results_") and f.endswith(".json")]
    
    if not files:
        print("No result files found to cluster.")
        return

    for filename in files:
        file_path = os.path.join(output_dir, filename)
        prompt_name = filename.replace("redteam_divi_results_", "").replace(".json", "")
        
        print(f"\nProcessing clustering for system prompt: {prompt_name}")
        
        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                data = json.load(f)
        except Exception as e:
            print(f"Error loading {filename}: {e}")
            continue
            
        if not data:
            print(f"No data in {filename}, skipping.")
            continue
        
        total_items = len(data)
        if total_items < 5:
            print(f"Not enough data points ({total_items}) for clustering. Skipping.")
            continue

        # Generate embeddings
        print(f"Generating embeddings for {len(data)} items...")
        group_texts = [f"{r.get('prompt', '')} {r.get('response', '')}" for r in data]
        
        # Use simple model
        group_embeddings = SentenceTransformer('paraphrase-multilingual-mpnet-base-v2').encode(group_texts)

        # Preprocess embeddings
        group_embeddings = np.nan_to_num(group_embeddings, nan=0.0)
        X_norm = normalize(group_embeddings, norm='l2')
        scaler = StandardScaler()
        X_input = scaler.fit_transform(X_norm)

        D = X_input.shape[1]
        
        # Run DIVI
        set_seed(42)
        divi_exp = DIVIClustering(split_threshold=None, split_interval=60, max_epochs=200, lr=0.01, verbose=True)
        divi_exp.split_threshold = 0.5 * D * (1 + np.log(2 * np.pi) + np.log(0.9))
        divi_exp.fit(X_input)

        _, _, log_p = divi_exp.model(torch.tensor(X_input, dtype=torch.float32))
        y_pred = torch.argmax(log_p, dim=1).detach().numpy()

        # Update clusters in data
        for i, r in enumerate(data):
            r['cluster'] = int(y_pred[i])

        # Save updated JSON
        with open(file_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"Updated results saved.")
        
        # Save Summary CSV
        csv_file = os.path.join(output_dir, f"redteam_divi_summary_{prompt_name}.csv")
        df = pd.DataFrame(data)
        df.to_csv(csv_file, index=False, encoding='utf-8-sig')
        print(f"Summary CSV saved to {csv_file}")


